                                    Back to school: Top tips for a healthy lunch box from a nutritionist
                                </h1>
                                <h2>How to prepare healthy lunches for your little ones</h2>
                                <div class="authors-info">
                                    <div class="author-name"><span>By</span> Time Out Dubai Things To Do</div>
                                    <div class="time">Posted: <span>Thursday August 27 2020</span></div>
                                </div>
                                <p>It’s nearly time for children across the UAE to go <a href="#">back to school</a> and if you’re looking for quick and easy ways to make their <a href="#">lunch box</a> a bit <a href="#">healthier</a>, read on.</p>
                                <p>We all know that packing lunches that filled with healthy (and delicious) options is extremely important and Nicola Selwood, Waitrose’s nutritionist, has shared eight tips for a healthy lunch box in time for back to <a href="#">school</a> season.</p>
                                <p>For your essential back-to-school guide, <a href="#">head here</a>.</p>
                                <h3>Aim to pack at least one of their five a day</h3>
                                <p>As a guide, a child’s portion is the amount of fruit or veg they can fit in their little hand. Dried fruit such as raisins counts too, but try not to include dried fruit too often as it’s usually high in sugar. Perfect lunch box snacks include bananas, sliced grapes, satsumas and clementines, apples, pears, carrot sticks, cucumber and sweet pepper slices. If your little one has an aversion to fruit and veggies, try sneaking them into a smoothie or cutting them up into fun shapes.</p>
                                <h3>Have a daily protein source</h3>
                                <p>Meat, fish, eggs and beans are great protein sources that can easily be incorporated into your child’s lunch box. Sliced chicken, egg mayonnaise or tuna are great sandwich fillers and sliced hard boiled eggs are a great source of protein (which also travel well in a lunch box). Need a change from sandwiches? Try adding mixed beans or tuna into a pasta salad.</p>
                                <h3>Include a starchy food every day</h3>
                                <p>Starchy foods such as bread, pasta, rice and cereals provide your little ones with energy, and try to pick wholegrain varieties where possible to help increase your child’s fibre intake. If your little one isn’t keen on the wholegrain versions of bread, pasta and rice then try the “half and half” method e.g. mixing half white, half wholegrain for pasta and rice, or mixing high fibre cereals into regular cereals. Starches usually make up the biggest part of a lunch box so it’s important to vary it. As this food group usually makes up the biggest part of lunch, it’s important to vary it. Think about a wholemeal wrap one day, pasta salad the next and so on.</p>
                                <h3>Add a daily portion of dairy to your child’s lunch box</h3>
                                <p>Dairy foods are an excellent source of calcium which is important for your little one’s growing bones. Milk is the top choice to have as a drink for lunch, but yoghurts and cheese sticks also make great additions to the lunch box rota. You can add cheese to sandwiches or cream cheese as a dip for vegetable sticks and if you’re vegan, alternatives such as soya milk or yogurt make great alternatives but try to pick varieties that are fortified with calcium. Other non-dairy sources of calcium include dried fruits such as figs and apricots.</p>
